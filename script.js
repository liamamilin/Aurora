  function initContract() {
    contract = new web3.eth.Contract(contractABI, contractAddress);
    setupEventListeners();
  }
  
  function setupEventListeners() {
    // 切换到水龙头页面
    document.getElementById('faucetTab').addEventListener('click', () => {
      document.getElementById('faucetSection').style.display = 'block';
      document.getElementById('depositSection').style.display = 'none';
    });
  
    // 切换到存取款页面
    document.getElementById('depositTab').addEventListener('click', () => {
      document.getElementById('faucetSection').style.display = 'none';
      document.getElementById('depositSection').style.display = 'block';
    });
  
    // 水龙头领取按钮
    document.getElementById('faucetButton').addEventListener('click', async () => {
      const accounts = await web3.eth.getAccounts();
      const userAccount = accounts[0];
      contract.methods.faucet().send({ from: userAccount })
        .on('transactionHash', hash => {
          document.getElementById('faucetResult').innerText = '交易发送中，哈希值：' + hash;
        })
        .on('receipt', receipt => {
          document.getElementById('faucetResult').innerText = '交易成功，区块哈希值：' + receipt.blockHash;
        })
        .on('error', error => {
          document.getElementById('faucetResult').innerText = '交易失败：' + error.message;
        });
    });
  
    // 存款按钮
    document.getElementById('depositButton').addEventListener('click', async () => {
      const accounts = await web3.eth.getAccounts();
      const userAccount = accounts[0];
      const amount = document.getElementById('depositAmount').value;
      const amountInWei = web3.utils.toWei(amount, 'ether');
      contract.methods.deposit(amountInWei).send({ from: userAccount })
        .on('transactionHash', hash => {
          document.getElementById('balanceOutput').innerText = '存款交易发送中，哈希值：' + hash;
        })
        .on('receipt', receipt => {
          document.getElementById('balanceOutput').innerText = '存款成功，区块哈希值：' + receipt.blockHash;
          updateBalance(userAccount);
        })
        .on('error', error => {
          document.getElementById('balanceOutput').innerText = '存款失败：' + error.message;
        });
    });
  
    // 取款按钮
    document.getElementById('withdrawButton').addEventListener('click', async () => {
      const accounts = await web3.eth.getAccounts();
      const userAccount = accounts[0];
      const amount = document.getElementById('withdrawAmount').value;
      const amountInWei = web3.utils.toWei(amount, 'ether');
      contract.methods.withdraw(amountInWei).send({ from: userAccount })
        .on('transactionHash', hash => {
          document.getElementById('balanceOutput').innerText = '取款交易发送中，哈希值：' + hash;
        })
        .on('receipt', receipt => {
          document.getElementById('balanceOutput').innerText = '取款成功，区块哈希值：' + receipt.blockHash;
          updateBalance(userAccount);
        })
        .on('error', error => {
          document.getElementById('balanceOutput').innerText = '取款失败：' + error.message;
        });
    });
  
    // 页面加载时获取并显示用户余额
    web3.eth.getAccounts()
      .then(accounts => {
        const userAccount = accounts[0];
        updateBalance(userAccount);
      })
      .catch(error => {
        console.error('获取账户失败', error);
      });
  }
  
  function updateBalance(account) {
    contract.methods.balanceOf(account).call()
      .then(balance => {
        const balanceInEther = web3.utils.fromWei(balance, 'ether');
        document.getElementById('balanceOutput').innerText = '当前余额：' + balanceInEther + ' ARA';
      })
      .catch(error => {
        console.error('获取余额失败', error);
      });
  }
  